""" Em Python """

# Modulo %, retorna o resto da divisão
print(10 % 3)

# Exponenciação
print(2 ** 3)

# No entanto, operacoes  entre numeros reais e inteiros retornam numeros reais
print(2 ** 3.0)

# Divisão exata, retorna um numero inteiro
print(10//3)

# No entanto, operacoes  entre numeros reais e inteiros retornam numeros reais
print(10//3.0)

# Divisão real, retorna um numero de ponto flutuante
print(10/3)

print(1 * 2 + 3 * 4)  # a multiplicacao ocerre antes da soma, resultando em 14
print(1 *(2 + 3) * 4)  # a operacao dentro dos parenteses ocorre antes, resultando em 20