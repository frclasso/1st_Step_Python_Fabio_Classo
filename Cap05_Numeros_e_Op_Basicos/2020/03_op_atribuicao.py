a = 21
b = 10
c = 0
d = a + b
e = 40

# And/ADD  +=
a += 1
print('O valor de "a" é: {}'.format(a))
print('É o mesmo que  a = a + 1')


# Subtract/AND -=
b -= 5
print('O valor de "b" é: {}'.format(b))
print('É o mesmo que  b = b - 5')


# Multiply/AND *=
d *= 2
print('O valor de "d" é: {}'.format(d))
print('É o mesmo que  d = d * 2')

# divide/AND /=
e /= 4
print('O valor de "e" é: {}'.format(e))
print('É o mesmo que  e = e / 4')

# Modulos/AND %=
f = 10
f %= 3
print('O valor de "f" é: {}'.format(f))
print('É o mesmo que  f = f % 3')

# Exponent/AND
g = 3
g **= 3
print('O valor de "g" é: {}'.format(g))
print('É o mesmo que  g = g ** 3')

# Floor division/AND //=
h = 10
h //= 3
print('O valor de "h" é: {}'.format(h))
print('É o mesmo que  h = h //= 3')



