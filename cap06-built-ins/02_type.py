#!/usr/bin/env python3

# a funcao type em Python recebe um objeto e retorna o tipo de objeto recebido

# <class 'range'>
r = range(0,30)
print(list(r))
print(type(r))
print(len(r))

# <class 'int'>
print(type(10))

# <class 'str'>
print(type('a'))
print(type('Ola Python!'))
print(len('Ola Python!'))