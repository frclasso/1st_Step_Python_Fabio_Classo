#!/usr/bin/env python3

"""O método pop () remove e retorna o último objeto ou obj da lista.

   Sintaxe: list.pop(obj=list[-1] """

list1 = ['physics', 'Biology', 'chemistry', 'maths']
list1.pop() # remove ultimo elemento da lista
print('List now: ', list1)

list1.pop(1) # remove elemento na posicao indice 1
print('List now: ', list1)

