#!/usr/bin/env python3

str = "Hello Python "

print(str) # imprime toda a string
print(str[0]) # imprime apenas o primeiro caractere da string
print(str[2:5]) # imprime do terceiro ao quinto caractere
print(str[2:]) # imprime a partir do terceiro caractere
print(str * 2) # imprime duas vezes toda a string
print(str + ' 3')