
# comparacao
# retorna True ou False

valor_1 = 100
valor_2 = 200

# igualdade
print(valor_1 == valor_2)

# desigualdade
print(valor_1 != valor_2) 

# maior que
print(valor_1 > valor_2)

# menor que
print(valor_1 < valor_2)

# maior ou igual
print(valor_2 >= 200.0)

# menor ou igual
print(valor_1 <= 100.01)