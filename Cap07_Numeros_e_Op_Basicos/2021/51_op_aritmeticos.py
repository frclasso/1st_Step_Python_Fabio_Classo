

# op aritimeticos

valor_1 = 100
valor_2 = 30

# adicao
resultado_1 = valor_1 + valor_2
print(f'Adição: {resultado_1}')

# subtracao
resultado_2 = valor_1 - valor_2 
print(f'Subtração: {resultado_2}')

# multiplicacao
resultado_3 = valor_1 * valor_2 
print(f'Multiplicação: {resultado_3}')

# divisao inteira
resultado_4 = valor_1 // valor_2 
print(f'Divisão inteira: {resultado_4}')

# divisao real
resultado_5 = valor_1 / valor_2 
print(f'Divisão real: {resultado_5}')

# Modulo
resultado_6 = valor_1 % valor_2 
print(f'Modulo: {resultado_6}')

# Exponencial
resultado_7 = valor_1 ** valor_2 
print(f'Exponencial: {resultado_7}')


